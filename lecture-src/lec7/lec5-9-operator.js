// 더하기, 두 수를 합치거나, 또는 두 문자를 하나로 붙일때 사용됩니다.
6 + 9;
"Hello " + "world!";

// 빼기, 곱하기, 나누기
9 - 3;
8 * 2;
9 / 3;

// 할당 연산자, 값을 변수에 할당
var myVariable = 'Bob';

// 일치 연산자, 두 값이 서로 같은지 다른지 확인하며, 이에 따라 true, false 를 반환합니다.
var myVariable = 3;
myVariable === 4; // false

// 부정 1
